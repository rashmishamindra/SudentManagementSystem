    private void updateTable(){
        String sql = "select sno AS ID, CONCAT(fname,' ',mname,' ',lname) AS Name, dob AS Date_Of_Birth, address AS Address, tp AS Telephon_No, mp AS Mobile_No from student";
        try {
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            Table_student.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (SQLException e) {
            System.out.println(e);
        }
    }
    
     public void generateStudentID(){
        try 
        {
            
            String sql = "SELECT MAX(right(sno,2)) as s from student";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            while(rs.next()){
                if(rs.first()== false){
                    sid.setText("S_00001");
                }
                else{
                    rs.last();
                    int id = rs.getInt(1)+1;
                    String s = String.valueOf(id);
                    int no = s.length();
                    for(int a=0; a<5-no; a++ ){
                       s = "0"+s;
                      
                    }
                    sid.setText("S_"+s);
                }
            }
        } 
        catch (Exception e) 
        {
            System.out.println(e);
        }
         
    }
     
   
