    private void Table_studentMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Table_studentMouseClicked
        try{
           int row = Table_student.getSelectedRow();
           String table_click = (Table_student.getModel().getValueAt(row, 0).toString());
           String sql = "select * from student where sno='"+table_click+"'";
           pst = conn.prepareStatement(sql);
           rs = pst.executeQuery();
           
           if(rs.next()){
               String add1 = rs.getString("sno");
               sid.setText(add1);
               String add2 = rs.getString("fname");
               fname.setText(add2);
               String add3 = rs.getString("mname");
               mname.setText(add3);
               String add4 = rs.getString("lname");
               lname.setText(add4);
               Date add5 = rs.getDate("dob");
               dob.setDate(add5);
               String add6 = rs.getString("address");
               TextArea_address.setText(add6);
               String add7 = rs.getString("tp");
               tel.setText(add7);
               String add8 = rs.getString("mp");
               mob.setText(add8);
             
           }
        }
        catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_Table_studentMouseClicked

