    private void Button_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_searchActionPerformed

        String s = Text_search.getText();
        String sql = "SELECT sno AS ID, CONCAT(fname,' ',mname,' ',lname) AS Name, dob AS Date_Of_Birth, address AS Address, tp AS Telephon_No, mp AS Mobile_No FROM student WHERE sno LIKE '%"+s+"%'OR fname LIKE '"+s+"%' OR lname LIKE '"+s+"%'  ";
        
        try{
        pst = conn.prepareStatement(sql);
        rs = pst.executeQuery();
        Table_student.setModel(DbUtils.resultSetToTableModel(rs));
        }
        catch(Exception e){
          System.out.print("Exception is trows "+ e);
      }
        
    }//GEN-LAST:event_Button_searchActionPerformed

