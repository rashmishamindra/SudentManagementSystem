    public void generateUserID(){
        try 
        {
            
            String sql = "SELECT MAX(right(userid,2)) as s from user";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            while(rs.next()){
                if(rs.first()== false){
                    Text_userid.setText("U_001");
                }
                else{
                    rs.last();
                    int id = rs.getInt(1)+1;
                    String s = String.valueOf(id);
                    int no = s.length();
                    for(int a=0; a<3-no; a++ ){
                       s = "0"+s;
                      
                    }
                    Text_userid.setText("U_"+s);
                }
            }
        } 
        catch (Exception e) 
        {
            System.out.println(e);
        }
    }
     
