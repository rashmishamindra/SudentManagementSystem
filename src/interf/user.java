    private void Button_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_saveActionPerformed
            
        
        try
        {
            
            if(ComboBox_branch.getSelectedItem().equals("SELECT")){
                jLabel6.setVisible(true);
                JOptionPane.showMessageDialog(null,"Please select User Type.");
            }else{
                jLabel6.setVisible(false);
            }
            if(Text_username.getText().equals("")){
                jLabel7.setVisible(true);
                JOptionPane.showMessageDialog(null,"Please enter your name.");
                 //Button_save.setEnabled(false);
            }else{
                jLabel7.setVisible(false);
               // Button_save.setEnabled(true);
            }
            
            String pw_pattern = "^(?=.*\\d)(?=.*[a-z])(?=.*[A-Z]).{8,15}$";
            Pattern pattern = Pattern.compile(pw_pattern);
            Matcher regexMatcher = pattern.matcher(Text_password.getText());
            if(!regexMatcher.matches()){
                jLabel8.setVisible(true); 
                JOptionPane.showMessageDialog(null,"Password fomat is not correct.Password must be at least 8 characters, no more than 15 characters, and must include at least one upper case letter, one lower case letter, and one numeric digit.");
            }else{
                jLabel8.setVisible(false);
            }
            
            
           
            if(ComboBox_branch.getSelectedItem().equals("SELECT") || Text_username.getText().equals("") || !regexMatcher.matches() )
            {
                JOptionPane.showMessageDialog(null,"Error, Please check and then re-enter ");
            }
            else{
            String sql = "Insert into user(userid, usertype, username, password) values (?,?,?,?)";

            pst = conn.prepareStatement(sql);
            pst.setString(1,Text_userid.getText() );
            pst.setString(2,ComboBox_branch.getSelectedItem().toString() );
            pst.setString(3,Text_username.getText() );
            pst.setString(4,Text_password.getText() );
           
            pst.execute();

            JOptionPane.showMessageDialog(null, "Save Successful");
            Text_userid.setText("");
            ComboBox_branch.setSelectedItem("SELECT");
            Text_username.setText("");
            Text_password.setText("");

            generateUserID();
            
//            login l = new login();
//            l.setVisible(true);
//            this.dispose();
        }}
        catch(Exception e)
        {
            System.out.println(e);
        }
        
    }//GEN-LAST:event_Button_saveActionPerformed

