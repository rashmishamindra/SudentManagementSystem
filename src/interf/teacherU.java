    public void generateStudentID(){
        try 
        {
            String sql = "SELECT MAX(right(tno,2)) as s from teacher";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            while(rs.next()){
                if(rs.first()== false){
                    sid.setText("T_00001");
                }
                else{
                    rs.last();
                    int id = rs.getInt(1)+1;
                    String s = String.valueOf(id);
                    int no = s.length();
                    for(int a=0; a<5-no; a++ ){
                       s = "0"+s;
                      
                    }
                    sid.setText("T_"+s);
                }
            }            
        } 
        catch (Exception e) 
        {
            System.out.println(e);
        }
    }
