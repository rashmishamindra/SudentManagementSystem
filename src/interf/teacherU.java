    private void telKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_telKeyTyped
        char caracter = evt.getKeyChar();
        if (((caracter < '0') || (caracter > '9'))&& (caracter != '\b')) {
            evt.consume();
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(rootPane, "Cannot Input Characters!");
        }
        if (tel.getText().length() == 10 ){ // limit textfield to 10 characters
            evt.consume(); 
        } 
    }//GEN-LAST:event_telKeyTyped

